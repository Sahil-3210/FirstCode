public class Increment_with
{
    public static void main(String[] args) {
        char grade = 'A';
        grade = (char)(grade+8);
        System.out.println(grade);
        
        
        char Grade = 'I';
        Grade = (char)(Grade-8);
        System.out.println(Grade);
    }
}

